"""
This module provides basic mathematical utility functions.
"""


def add(first_number, second_number):
    """
    Return the sum of first_number and second_number.

    Args:
        first_number (int or float): First number.
        second_number (int or float): Second number.

    Returns:
        int or float: The sum of first_number and second_number.
    """

    return first_number + second_number


def subtract(first_number, second_number):
    """
    Return the difference of first_number and second_number.

    Args:
        first_number (int or float): First number.
        second_number (int or float): Second number.

    Returns:
        int or float: The difference of first_number and second_number.
    """

    return first_number - second_number


def multiply(first_number, second_number):
    """
    Return the product of first_number and second_number.

    Args:
        first_number (int or float): First number.
        second_number (int or float): Second number.

    Returns:
        int or float: The product of first_number and second_number.
    """

    return first_number * second_number


def divide(first_number, second_number):
    """
    Return the division of first_number and second_number.

    Args:
        first_number (int or float): First number.
        second_number (int or float): Second number.

    Returns:
        int or float: The divison of first_number and second_number.
    """
    return first_number / second_number
